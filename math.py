# Quick review of some Python math elements

x = 0
y = 0

print("x currently = ", x)
print("y currently = ", y)
print(f"x + y = {x+y}")
print(f"x - y = {x-y}")
x = 2
y = 7
print("Now x is ", x)
print("And now y is ", y)
print(f"x + y now is {x+y}")
print(f"x - y is now {x-y}")
print(f"x * y is {x*y}")
print(f"x / y is {x/y}")
